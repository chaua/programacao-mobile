class AloMamae {


    /**
     * Imprime a mensagem no console.
     */
    fun imprime() {
        println("Alô, mamãe!")
    }

    /**
     * Retorna a soma de dois valores.
     *
     * @param a primeiro valor
     * @param b segundo valor
     *
     * @return soma de a + b
     */
    fun soma(a: Int, b: Int):Int {
        return a + b
    }

}